/*
    All alternate functions are defined through Src/stm32f4xx_hal_msp.c
    generated by STM32CubeMX, partly copied to *.ino file.

    I2S1: LRCK->PA4, BCK->PA5, DATA->PA7
    SDIO: CMD->PD2, SCK->PC12
          D0->PC8, D1->PC9, D2->PC10, D3->PC11
*/

#include <Arduino.h>

// I2C1 for PCM1863
#define I2C1_SDA PB_7
#define I2C1_SCL PB_6

// I2C3 for IP5108
#define I2C3_SDA PB8
#define I2C3_SCL PA8

// RGB LED
#define LED_R PB_1_ALT1
#define LED_G PA_6
#define LED_B PB_0

// Misc
#define BTN_OK PB5
#define BATT_IRQ PA9
#define BUZZER PA3

// SDIO
#define SDIO_D0 PC8
#define SDIO_D1 PC9
#define SDIO_D2 PC10
#define SDIO_D3 PC11
#define SDIO_CMD PD2
#define SDIO_CLK PC12
#define SDIO_CD PC7